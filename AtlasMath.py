class BandDepth:

    @staticmethod
    def IndicatorBandDepth(curve, curveset, j=2):
        bandDepth = 0.0
        normalizingValue = 1.0/misc.comb(len(curveset), j)
        for testBandSet in itertools.combinations(curveset, j):
            bandDepth = bandDepth + BandDepth.Indicator(curve, testBandSet)
        return bandDepth*normalizingValue

    @staticmethod
    def ProportionalBandDepth(curve, curveset, j=2):
        bandDepth = 0.0
        normalizingValue = 1.0/misc.comb(len(curveset), j)
        for testBandSet in itertools.combinations(curveset, j):
            bandDepth = bandDepth + BandDepth.Proportion(curve, testBandSet)
        return bandDepth*normalizingValue

    @staticmethod
    def WeightedIndicatorBandDepth(key, curvedict, weightdict, j=2):
        curve = curvedict[key]
        numerator = 0.0
        denominator = 0.0
        for keys in itertools.combinations(curvedict.keys(), j):
            weightSubset = BandDepth.__ExtractSubset(weightdict, keys)
            testBandSet = BandDepth.__ExtractSubset(curvedict, keys)
            product = BandDepth.__ProductOfList(weightSubset)
            denominator += product
            numerator += product*BandDepth.Indicator(curve, testBandSet)
        return numerator/denominator

    @staticmethod
    def WeightedProportionalBandDepth(key, curvedict, weightdict, j=2):
        curve = curvedict[key]
        numerator = 0.0
        denominator = 0.0
        for keys in itertools.combinations(curvedict.keys(), j):
            weightSubset = BandDepth.__ExtractSubset(weightdict, keys)
            testBandSet = BandDepth.__ExtractSubset(curvedict, keys)
            product = BandDepth.__ProductOfList(weightSubset)
            denominator += product
            numerator += product*BandDepth.Proportion(curve, testBandSet)
        return numerator/denominator

    @staticmethod
    def Indicator(curve, bandset):
        '''
        returns 1 if the curve is within the band 
        generated by the bandset for all x. 0 otherwise
        '''
        for x in range(len(curve)):
            bandRange = BandDepth.__BandBounds(bandset, x)
            if curve[x] < bandRange[0] or curve[x] > bandRange[1]:
                return 0.0
        return 1.0

    @staticmethod
    def Proportion(curve, bandset):
        '''
        return the proportion of the domain where 
        curve is betwen the band values
        '''
        proportion = 0.0
        for x in range(len(curve)):
            bandRange = BandDepth.__BandBounds(bandset, x)
            if (bandRange[0] > curve[x]) or (bandRange[1] < curve[x]):
                continue
            proportion += 1.
        return proportion/len(curve)

    @staticmethod
    def GenerateFences(minimum, maximum, median):
        upperfence = median + (maximum - median)*1.5
        lowerfence = median - (median - minimum)*1.5
        return [upperfence, lowerfence]

    @staticmethod
    def __BandBounds(bandset, x):
        minVal = float("inf")
        maxVal = float("-inf")

        for curve in bandset:
            if curve[x] < minVal:
                minVal = curve[x]
            if curve[x] > maxVal:
                maxVal = curve[x]

        return [minVal, maxVal]

    @staticmethod
    def __ExtractSubset(objectdict, keytuple):
        output = []
        for key in keytuple:
            output.append(objectdict[key])
        return output

    @staticmethod
    def __ProductOfList(numericallist):
        r = 1.
        for i in numericallist:
            r *= i
        return r
