import numpy as np
import scipy.misc as misc
import itertools

################################


class Weighting:

    @staticmethod
    def GenerateWeights(data, stdev, center):
        weights = []
        running_sum = 0.
        for value in data:
            w = np.exp(-((value - center)**2.)/(2.*(stdev**2.)))
            weights.extend([w])
            running_sum += w
        return [i/running_sum for i in weights]


